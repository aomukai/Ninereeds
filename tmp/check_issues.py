found_any = False
for t in targets:
    p = Path(t)
    if not p.exists():
        continue
    text = p.read_text()
    lines = text.split("\n")
    for i, ln in enumerate(lines):
        if ln.strip() == "[Ninereeds]" and i+1 < len(lines) and lines[i+1].strip() == "":
            print(f"BLANK {t}:{i+1}")
            found_any = True
    if "=== END ===" in text:
        print(f"ENDMARKER {t}")
        found_any = True
    if re.search(r"^```\s*$", text, re.MULTILINE):
        print(f"FENCE {t}")
        found_any = True
    uc = len(re.findall(r"^\[user\]", text, re.MULTILINE))
    nc = len(re.findall(r"^\[Ninereeds\]", text, re.MULTILINE))
    if uc != 4 or nc != 4:
        print(f"TAGMISMATCH {t} u={uc} n={nc}")
        found_any = True

if not found_any:
    print(f"All {len(targets)} files clean - no issues found")
